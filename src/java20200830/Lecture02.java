package java20200830;

public class Lecture02 {

	public static void main(String[] args) {
		int counter = 1;
		System.out.println("counter의 값은" + counter);
		
		counter++;
		System.out.println("counter의 값은" + counter);
		System.out.println("counter의 값은" + counter++);
		System.out.println("counter의 값은" + counter);
		System.out.println("counter의 값은" + --counter);
	}

}
