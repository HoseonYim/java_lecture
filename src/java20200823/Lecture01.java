package java20200823;

public final class Lecture01 {

	public static void main(String[] args) {
		
		// 자바 변수, 자료형(타입)
		
		//자료형 변수형 연산자 값;
		int age = 13;                  //정수형
		double pi = 3.14159265359;    //실수형
		boolean flag = true;           //논리형
		String name = "바보";           //문자열
		
		System.out.println("정수형 변수 age의 값은 : " + age);
		System.out.println("실수형 변수 pi의 값은 : " + pi);
		System.out.println("논리형 변수 flag의 값은 : " +  flag);
		System.out.println("문자열 변수 name의 값은 : " + name);
	}

}
