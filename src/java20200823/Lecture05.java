package java20200823;

public class Lecture05 {

	public static void main(String[] args) {
		//반복문
		
		// 초기식; 조건식; 증감식(++);
		for(int i = 0; i < 10; i++) {
			System.out.println("i의 값은 : " + i);
		}
		
		System.out.println("===================");
		
		// 초기식; 조건식; 증감식(--);
		for(int i = 10; i > 0; i--) {
			System.out.println("i의 값은 : " + i);
		}
	}

}
